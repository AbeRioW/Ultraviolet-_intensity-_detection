// 显示一个字符
void OLED_ShowChar(uint8_t x, uint8_t y, uint8_t chr, uint8_t size)
{
    uint8_t c = chr - ' ';
    uint8_t i, j;
    
    if(size == 8)  // 6x8字体
    {
        if(x > OLED_WIDTH - 6 || y > OLED_PAGES - 1) return;
        
        for(i = 0; i < 6; i++)
        {
            OLED_GRAM[y][x + i] = F6x8[c][i];
        }
    }
    else if(size == 16)  // 8x16字体
    {
        if(x > OLED_WIDTH - 8 || y > OLED_PAGES - 2) return;
        
        for(i = 0; i < 8; i++)
        {
            OLED_GRAM[y][x + i] = F8X16[c * 16 + i];
            OLED_GRAM[y + 1][x + i] = F8X16[c * 16 + i + 8];
        }
    }
}

// 显示字符串
void OLED_ShowString(uint8_t x, uint8_t y, uint8_t *chr, uint8_t size)
{
    uint8_t j = 0;
    while(chr[j] != '\0')
    {
        OLED_ShowChar(x, y, chr[j], size);
        if(size == 8) x += 6;
        else if(size == 16) x += 8;
        
        if(x > OLED_WIDTH - (size == 8 ? 6 : 8))
        {
            x = 0;
            y += (size == 8 ? 1 : 2);
        }
        j++;
    }
}

// 显示数字
void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len, uint8_t size)
{
    uint8_t t, temp;
    uint8_t enshow = 0;
    
    for(t = 0; t < len; t++)
    {
        temp = (num / (uint32_t)pow(10, len - t - 1)) % 10;
        if(enshow == 0 && t < (len - 1))
        {
            if(temp == 0)
            {
                OLED_ShowChar(x + (size == 8 ? 6 : 8) * t, y, ' ', size);
                continue;
            }
            else
            {
                enshow = 1;
            }
        }
        OLED_ShowChar(x + (size == 8 ? 6 : 8) * t, y, temp + '0', size);
    }
}

