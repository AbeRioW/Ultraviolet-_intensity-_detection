// 显示BMP图片
void OLED_DrawBMP(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, uint8_t BMP[])
{
    uint16_t j = 0;
    uint8_t x, y;
    
    if(y1 % 8 == 0) y = y1 / 8;
    else y = y1 / 8 + 1;
    
    for(y = y0; y < y1; y++)
    {
        OLED_SetPos(x0, y);
        for(x = x0; x < x1; x++)
        {
            OLED_WriteData(BMP[j++]);
        }
    }
}

// 刷新显存到OLED
void OLED_Refresh(void)
{
    uint8_t i, j;
    for(i = 0; i < OLED_PAGES; i++)
    {
        OLED_SetPos(0, i);
        for(j = 0; j < OLED_WIDTH; j++)
        {
            OLED_WriteData(OLED_GRAM[i][j]);
        }
    }
}

